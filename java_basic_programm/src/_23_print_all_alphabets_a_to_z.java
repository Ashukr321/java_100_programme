public class _23_print_all_alphabets_a_to_z {
    public static void main(String[] args) {
        // printing the all the alphabets from a to z
        char ch ; // char take 2 byte -> 16 bits in the memory
        for (ch= 'a'; ch<='z'; ch++){
            System.out.println(ch);
        }
    }
}
